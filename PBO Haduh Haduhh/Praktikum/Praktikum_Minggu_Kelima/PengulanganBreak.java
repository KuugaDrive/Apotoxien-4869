public class PengulanganBreak {

    public static void main(String[]args) {
    	int S;
    	for(S=0; S<=10; S++){
    		System.out.println("Perulangan S yang Ke-"+S);
    		if(S>=5){
    			break;
    		}
    	}
    }
    
    
}